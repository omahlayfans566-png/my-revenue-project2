const RECENT_EMOJIS_KEY = "dateclone_recent_emojis";
const MAX_RECENT = 30;

const EmojiPicker = ({ onSelect, show = false, onClose }: EmojiPickerProps) => {
    const ref = useRef<HTMLDivElement>(null);
    const [activeCategory, setActiveCategory] = useState(0);
    const [searchQuery, setSearchQuery] = useState("");
    const [recentEmojis, setRecentEmojis] = useState<string[]>(() => {
        try {
            const stored = localStorage.getItem(RECENT_EMOJIS_KEY);
            return stored ? JSON.parse(stored) : [];
        } catch { return []; }
    });

    // Save recent emojis
    const addRecentEmoji = useCallback((emoji: string) => {
        setRecentEmojis(prev => {
            const filtered = prev.filter(e => e !== emoji);
            const updated = [emoji, ...filtered].slice(0, MAX_RECENT);
            try { localStorage.setItem(RECENT_EMOJIS_KEY, JSON.stringify(updated)); } catch { /* */ }
            return updated;
        });
    }, []);

    // Click outside to close
    useEffect(() => {
        const handleClickOutside = (e: MouseEvent) => {
            if (ref.current && !ref.current.contains(e.target as Node)) {
                onClose?.();
            }
        };
        if (show) {
            document.addEventListener("mousedown", handleClickOutside);
        }
        return () => document.removeEventListener("mousedown", handleClickOutside);
    }, [show, onClose]);

    // Filtered emojis based on search
    const filteredData = useMemo(() => {
        if (!searchQuery.trim()) return null;
        const q = searchQuery.toLowerCase();
        const results: { category: string; emojis: string[] }[] = [];
        for (const cat of EMOJI_DATA) {
            const matched = cat.emojis.filter(e => e.toLowerCase().includes(q));
            if (matched.length > 0) {
                results.push({ category: cat.category, emojis: matched });
            }
        }
        return results;
    }, [searchQuery]);

    const handleSelect = (emoji: string) => {
        addRecentEmoji(emoji);
        onSelect(emoji);
    };

    if (!show) return null;

    const displayData = filteredData || EMOJI_DATA;
    const hasRecent = recentEmojis.length > 0 && !searchQuery.trim();

    return (
        <div className="emoji-picker" ref={ref} role="dialog" aria-label="Emoji picker">
            {/* Search bar */}
            <div className="emoji-search">
                <input
                    type="text"
                    placeholder="Search emojis..."
                    value={searchQuery}
                    onChange={e => setSearchQuery(e.target.value)}
                    autoFocus
                    aria-label="Search emojis"
                />
                {searchQuery && (
                    <button className="emoji-search-clear" onClick={() => setSearchQuery("")} aria-label="Clear search">вң•</button>
                )}
            </div>

            {/* Categories */}
            <div className="emoji-categories" role="tablist">
                {hasRecent && (
                    <button
                        className={`emoji-cat-btn ${activeCategory === -1 ? "active" : ""}`}
                        onClick={() => setActiveCategory(-1)}
                        role="tab"
                        aria-selected={activeCategory === -1}
                        aria-label="Recent"
                        title="Recent"
                    >
                        рҹ•җ
                    </button>
                )}
                {EMOJI_DATA.map((cat, i) => (
                    <button
                        key={cat.category}
                        className={`emoji-cat-btn ${i === activeCategory ? "active" : ""}`}
                        onClick={() => setActiveCategory(i)}
                        role="tab"
                        aria-selected={i === activeCategory}
                        aria-label={cat.category}
                        title={cat.category}
                    >
                        {cat.emojis[0]}
                    </button>
                ))}
            </div>

            {/* Emoji grid */}
            <div className="emoji-grid" role="tabpanel">
                {filteredData && filteredData.length === 0 ? (
                    <div className="emoji-no-results">No emojis found</div>
                ) : hasRecent && activeCategory === -1 ? (
                    <>
                        <div className="emoji-category-label">Recently Used</div>
                        {recentEmojis.map(emoji => (
                            <button
                                key={emoji}
                                className="emoji-btn"
                                onClick={() => handleSelect(emoji)}
                                aria-label={emoji}
                            >
                                {emoji}
                            </button>
                        ))}
                    </>
                ) : filteredData ? (
                    filteredData.map(cat => (
                        <div key={cat.category}>
                            <div className="emoji-category-label">{cat.category}</div>
                            {cat.emojis.map(emoji => (
                                <button
                                    key={emoji}
                                    className="emoji-btn"
                                    onClick={() => handleSelect(emoji)}
                                    aria-label={emoji}
                                >
                                    {emoji}
                                </button>
                            ))}
                        </div>
                    ))
                ) : (
                    <>
                        {hasRecent && activeCategory === -1 ? (
                            <>
                                <div className="emoji-category-label">Recently Used</div>
                                {recentEmojis.map(emoji => (
                                    <button
                                        key={emoji}
                                        className="emoji-btn"
                                        onClick={() => handleSelect(emoji)}
                                        aria-label={emoji}
                                    >
                                        {emoji}
                                    </button>
                                ))}
                            </>
                        ) : (
                            <>
                                <div className="emoji-category-label">{EMOJI_DATA[activeCategory]?.category}</div>
                                {EMOJI_DATA[activeCategory]?.emojis.map(emoji => (
                                    <button
                                        key={emoji}
                                        className="emoji-btn"
                                        onClick={() => handleSelect(emoji)}
                                        aria-label={emoji}
                                    >
                                        {emoji}
                                    </button>
                                ))}
                            </>
                        )}
                    </>
                )}
            </div>
        </div>
    );
};

export default EmojiPicker;